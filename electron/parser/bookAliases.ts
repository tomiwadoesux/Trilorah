/**
 * Get canonical book name from alias
 */
export function getCanonicalBook(alias: string): string | null {
  const lowerAlias = alias.toLowerCase();

  for (const [canonical, aliases] of Object.entries(BOOK_ALIASES)) {
    if (aliases.includes(lowerAlias)) {
      return canonical;
    }
  }

  return null;
}
