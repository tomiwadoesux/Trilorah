/* ---------- HELPER FUNCTION ---------- */
// Generates numbered book aliases from base aliases
function numberedAliases(num: 1 | 2 | 3, baseAliases: string[]): string[] {
  const prefixes = {
    1: ["1", "first", "1st", "one", "won", "once", "on"],
    2: ["2", "second", "2nd", "two", "too", "to", "tu"],
    3: ["3", "third", "3rd", "three", "tree", "thru"],
  };

  const result: string[] = [];
  for (const prefix of prefixes[num]) {
    for (const base of baseAliases) {
      result.push(`${prefix} ${base}`);
      result.push(`${base} ${prefix.replace(/\D/g, "") || num}`); // "samuel 1"
    }
  }
  return result;
}

